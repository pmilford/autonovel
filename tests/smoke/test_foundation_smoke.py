"""Tier-3 smoke: the PR 3 foundation commands on Claude Code.

One test per command — `gen-world`, `gen-characters`, `gen-outline`,
`voice-discovery`, `gen-canon`. Each starts from a copy of the
`tiny-series-historical` fixture with the command's write target reset
to the template placeholder, runs the real runtime, and asserts the
acceptance block from the corresponding `commands/*.md`.

Opt-in under `@pytest.mark.smoke`. Skips cleanly when `claude` is not on
`$PATH` (see `tests/smoke/conftest.py`). Subscription auth is primary;
the conftest strips `ANTHROPIC_API_KEY` unless `AUTONOVEL_SMOKE_USE_API_KEY=1`.

These are cheap-ish (each command is one model call), but still real
spend. Run individually with `-k gen_world` etc. when iterating.
"""

from __future__ import annotations

import re
from pathlib import Path

import pytest

from .conftest import run_command_in_runtime


PLACEHOLDER_WORLD = "# World\n\n<!-- Generated by /autonovel:gen-world. Leave empty until then. -->\n"
PLACEHOLDER_CHARACTERS = (
    "# Characters\n\n<!-- Generated by /autonovel:gen-characters. Leave empty until then. -->\n"
)
PLACEHOLDER_OUTLINE = "# Outline\n\n<!-- Generated by /autonovel:gen-outline. Leave empty until then. -->\n"
PLACEHOLDER_VOICE_PART2 = (
    "# Voice\n\n"
    "## Part 1 — Series voice\n"
    "Close third, past tense. Period-accurate to 1520 Venice in vocabulary and\n"
    "sensibility; avoid modern idiom or clinical 21st-century observations.\n\n"
    "## Part 2 — Book fingerprint\n\n"
    "<!-- Filled by /autonovel:voice-discovery. Leave empty until then. -->\n"
)
PLACEHOLDER_CANON = (
    "# Canon\n\n"
    "Hard facts true across all books in this series. Each entry is a single\n"
    "verifiable claim; commands reference it by the headline in brackets.\n\n"
    "<!-- Seeded by /autonovel:gen-canon; grown by /autonovel:promote-canon. -->\n"
)


def _word_count(text: str) -> int:
    body = re.sub(r"<!--.*?-->", "", text, flags=re.DOTALL)
    return len(body.split())


@pytest.mark.smoke
@pytest.mark.genre("historical")
def test_gen_world(tmp_runtime_series) -> None:
    series = tmp_runtime_series("tiny-series-historical")
    target = series.path / "shared" / "world.md"
    target.write_text(PLACEHOLDER_WORLD, encoding="utf-8")

    result = run_command_in_runtime(
        runtime="claude",
        command="/autonovel:gen-world",
        cwd=series.path,
        allowed_tools=["Read", "Write", "Bash"],
    )
    assert result.returncode == 0, (
        f"claude returned {result.returncode}\nstdout:\n{result.stdout}\n"
        f"stderr:\n{result.stderr}"
    )

    text = target.read_text(encoding="utf-8")
    assert text.lstrip().startswith("# World"), "missing top-level heading"
    assert _word_count(text) >= 400, f"world body too short: {_word_count(text)} words"
    # Not just the placeholder left over.
    assert "Generated by /autonovel:gen-world" not in text or _word_count(text) >= 400


