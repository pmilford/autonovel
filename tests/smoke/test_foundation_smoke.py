@pytest.mark.smoke
@pytest.mark.genre("historical")
def test_voice_discovery(tmp_runtime_series) -> None:
    series = tmp_runtime_series("tiny-series-historical")
    target = series.path / "books" / "tiny-inquisitor" / "voice.md"
    target.write_text(PLACEHOLDER_VOICE_PART2, encoding="utf-8")

    result = run_command_in_runtime(
        runtime="claude",
        command="/autonovel:voice-discovery --book tiny-inquisitor",
        cwd=series.path,
        allowed_tools=["Read", "Write", "Bash"],
    )
    assert result.returncode == 0, (
        f"claude returned {result.returncode}\nstdout:\n{result.stdout}\n"
        f"stderr:\n{result.stderr}"
    )

    text = target.read_text(encoding="utf-8")
    assert "## Part 1" in text, "Part 1 must be preserved verbatim"
    assert "Close third, past tense" in text, "Part 1 contents dropped"
    assert "## Part 2" in text, "Part 2 heading missing"
    # Part 2 body must not be only the placeholder comment.
    part2 = text.split("## Part 2", 1)[1]
    body = re.sub(r"<!--.*?-->", "", part2, flags=re.DOTALL).strip()
    assert body, "Part 2 is empty after voice-discovery"
    assert len(body.split()) >= 40, f"Part 2 is too short: {len(body.split())} words"


