@pytest.mark.smoke
@pytest.mark.genre("historical")
def test_sidequest_menu_is_read_only(tmp_runtime_series) -> None:
    """Sidequest dispatcher must not write files. Contract rather than cost.

    We can't interactively pick an option from a subprocess, but we can
    verify the menu prints and no write targets are touched.
    """
    series: Path = tmp_runtime_series("tiny-series-historical").path
    before = _snapshot(series)

    result = run_command_in_runtime(
        runtime="claude",
        command="/autonovel:sidequest",
        cwd=series,
        allowed_tools=["Read", "Bash"],
        timeout=180,
    )
    # Non-interactive `claude -p` exits after one turn; we only care that
    # the run was clean and nothing changed on disk.
    assert result.returncode == 0, (
        f"claude returned {result.returncode}\nstdout:\n{result.stdout}\n"
        f"stderr:\n{result.stderr}"
    )
    assert "gen-world" in result.stdout or "gen-world" in result.stderr, (
        "menu did not list foundation commands"
    )
    after = _snapshot(series)
    assert before == after, "sidequest dispatcher modified files (must be read-only)"


def _snapshot(root: Path) -> dict[str, bytes]:
    """Cheap content snapshot for read-only assertion. Ignores .autonovel/."""
    out: dict[str, bytes] = {}
    for p in root.rglob("*"):
        if not p.is_file():
            continue
        rel = p.relative_to(root)
        # `.autonovel/command-log.jsonl` gets appended even for read-only
        # commands (via the postamble); that's lifecycle bookkeeping, not a
        # content write. Exclude the whole `.autonovel/` tree from the
        # "must not change" assertion.
        if rel.parts and rel.parts[0] == ".autonovel":
            continue
        out[str(rel)] = p.read_bytes()
    return out
